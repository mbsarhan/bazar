    <h1>Video Player with Quality Selector</h1>

    @if(session('success'))
        <div style="padding: 15px; margin-bottom: 20px; border-radius: 4px; width: 100%; max-width: 770px; box-sizing: border-box; background-color: #d4edda; color: #155724; border: 1px solid #c3e6cb;">
