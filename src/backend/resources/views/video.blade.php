    <div class="video-container">
        {{-- The video tag is now simpler. No <source> tag. We will add it via JavaScript. --}}
        {{-- Added the 'vjs-theme-city' class for a nicer theme --}}
        <video id="my-player" class="video-js vjs-theme-city vjs-big-play-centered" controls preload="auto"></video>
    </div>
    
    <p style="margin-top: 20px; font-size: 0.9em; color: #666;">Loading video from: <code>{{ $videoUrl }}</code></p>
    
    <!-- 2. Include Video.js JavaScript -->
    <script src="https://vjs.zencdn.net/8.10.0/video.min.js"></script>

    <!-- 3. Include the HLS Quality Selector Plugin -->
    <script src="https://cdn.jsdelivr.net/npm/videojs-hls-quality-selector@1.1.4/dist/videojs-hls-quality-selector.min.js"></script>

    <script>
        document.addEventListener('DOMContentLoaded', function () {
            // 4. Get the video source URL generated by Laravel
            const videoSrc = @json($videoUrl);

            // 5. Define the player options in an object
            const options = {
                controls: true,
                autoplay: 'muted',
                preload: 'auto',
                fluid: true, // Makes the player responsive
                sources: [{ // Set the source directly in the options
                    src: videoSrc,
                    type: 'application/x-mpegURL'
                }],
                plugins: {
                    // This is the most reliable way to enable the plugin
                    hlsQualitySelector: {
                        displayCurrentQuality: true, // Optional: Shows the current quality (e.g., 720p)
                    }
                }
            };

            // 6. Initialize the Video.js player with the options
            const player = videojs('my-player', options);

            player.ready(function() {
                console.log('Player is ready.');
                // At this point, the player, the plugin, and the source are all loaded together.
            });
        });
    </script>

